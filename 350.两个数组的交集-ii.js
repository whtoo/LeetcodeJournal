var intersect = function(nums1, nums2) {
    if(nums1.length > nums2.length) intersect(nums2,nums1)

    let m = new Map()
    console.log(new Set(nums1.values()))
    for(const v of nums1){
        if(m.has(v)){
            let cnt = m.get(v)
            m.set(v,++cnt)
        } else{
            m.set(v,1)
        }
    }

    let ret = []
    for(const n of nums2){
        let cnt = m.get(n)
        if(cnt > 0){
            ret.push(n)
            m.set(n,--cnt)
        }
    }
    return ret
};
